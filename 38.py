number1 = int(input("Enter Number : "))

if(number1 > 0 and number1 <= 7):
    print("Week One")
elif(number1 > 7 and number1 <= 14):
    print("Week Two")
elif(number1 > 14 and number1 <= 21):
    print("Week Three")
elif(number1 > 21 and number1 <= 28):
    print("Week Four")
elif(number1 > 28 and number1 <= 30):
    print("Week Five")